def _find_task_dir(task_id: str) -> Path | None:
    candidates = list(TASKS_DIR.glob(f"{task_id}*"))
    # strip _claude-code_YYYYMMDD_HHMMSS suffix if user passed a snapshot name
    for c in candidates:
        if c.is_dir() and (c / "spec.yaml").exists():
            return c
    return None


def _rejudge(snapshot_dir: Path, judge_model: str | None = None) -> None:
    # Import here to avoid slow startup when just viewing
    from unrealbench.src.ue_judge import run_unreal_judge

    result_path = snapshot_dir / "result.json"
    if not result_path.exists():
        print(f"[rejudge] no result.json in {snapshot_dir}")
        return

    d = json.loads(result_path.read_text())
    task_id = d.get("task_id", "")

    task_dir = _find_task_dir(task_id)
    if task_dir is None:
        print(f"[rejudge] cannot locate task directory for {task_id}")
        return

    spec_path = task_dir / "spec.yaml"
    if not spec_path.exists():
        print(f"[rejudge] no spec.yaml in {task_dir}")
        return

    # parse spec for instruction + files_to_edit
    import yaml  # type: ignore
    spec = yaml.safe_load(spec_path.read_text())
    instruction = spec.get("instruction", "")
    files_to_edit = spec.get("files_to_edit", [])

    # find test file
    test_files = sorted((task_dir / "tests").glob("*.cpp"))
    test_file = test_files[0] if len(test_files) == 1 else None

    solution_dir = task_dir / "solution"

    print(f"[rejudge] running judge on {snapshot_dir.name} ...")
    effective_judge_model = (judge_model or os.environ.get("UNREALBENCH_JUDGE_MODEL", "")).strip()
    judge_kwargs = {"judge_model": effective_judge_model} if effective_judge_model else {}
    result = run_unreal_judge(
        snapshot_dir=snapshot_dir,
        task_instruction=instruction,
        files_to_edit=files_to_edit,
        solution_dir=solution_dir,
        test_file=test_file,
        **judge_kwargs,
    )

    if result.get("skipped"):
        print(f"[rejudge] skipped: {result.get('reason')}")
        return

    persisted = result.get("persisted_judge", {})
    effective_model = (persisted.get("model") or effective_judge_model or d.get("judge_model") or "").strip()
    if effective_model.lower().startswith("gpt"):
        reasoning_effort = os.environ.get("UNREALBENCH_GPT_JUDGE_REASONING_EFFORT", "high").strip() or "high"
        persisted["reasoning_effort"] = reasoning_effort
        d["judge_reasoning_effort"] = reasoning_effort
    d["judge"] = persisted
    d["judge_model"] = effective_model or d.get("judge_model")
    result_path.write_text(json.dumps(d, indent=2), encoding="utf-8")
    print("[rejudge] done - result.json updated")
    _print_results(snapshot_dir)


