def _pass_fail(ok: bool) -> str:
    return "PASS" if ok else "FAIL"

def _print_results(snapshot_dir: Path) -> None:
    result_path = snapshot_dir / "result.json"
    if not result_path.exists():
        print(f"  [no result.json in {snapshot_dir.name}]")
        return

    d = json.loads(result_path.read_text())

    task_id   = d.get("task_id", "?")
    compile_  = d.get("compile_success", "?")
    passed    = d.get("task_passed", "?")
    tp        = d.get("tests_passed", "?")
    tr        = d.get("tests_run", "?")
    reason    = d.get("failure_reason") or ""
    model     = d.get("model", "?")
    cost      = d.get("cost_usd")
    dur       = d.get("duration_seconds")
    steps     = d.get("agent_steps")
    usage     = d.get("token_usage") or {}
    out_tok   = usage.get("output_tokens")

    header = (
        f"{'='*70}\n"
        f"  {task_id}   model={model}\n"
        f"  compile={compile_}  task_passed={passed}  "
        f"tests={tp}/{tr}  {reason}\n"
    )
    metric_parts = []
    cost_suffix = " est" if d.get("cost_usd_is_estimate") else ""
    token_suffix = " est" if d.get("token_usage_is_estimate") else ""
    if cost is not None:
        metric_parts.append(f"cost=${cost:.4f}{cost_suffix}")
    if out_tok is not None:
        metric_parts.append(f"output_tokens={out_tok}{token_suffix}")
    if steps is not None:
        metric_parts.append(f"agent_steps={steps}")
    if dur is not None:
        metric_parts.append(f"duration={dur:.0f}s")
    if metric_parts:
        header += "  " + "  ".join(metric_parts) + "\n"
    header += "=" * 70
    print(header)

    # test breakdown
    for t in d.get("test_results", []):
        icon = _pass_fail(t.get("passed", False))
        print(f"  [{icon}] {t['id']}")

    # judge block
    judge = d.get("judge") or {}
    if judge:
        print()
        trust = judge.get("trust_score")
        counts = judge.get("counts", {})
        mode   = judge.get("mode", "?")
        print(
            f"  Judge ({mode})  trust={trust}  "
            f"TP={counts.get('TP',0)} TN={counts.get('TN',0)} "
            f"FP={counts.get('FP',0)} FN={counts.get('FN',0)}"
        )
        for c in judge.get("classifications", []):
            label = c.get("classification", "?")
            tid   = c.get("test_id", "?").split(".")[-1]
            correct = c.get("model_code_correct_for_test", c.get("model_code_correct", "?"))
            rat   = (c.get("rationale") or "")[:140].replace("\n", " ")
            concerns = (c.get("concerns") or "").strip()
            print(f"  [{label}] {tid}  correct={correct}")
            print(f"       {rat}")
            if concerns:
                print(f"       ÃƒÆ’Ã‚Â¢Ãƒâ€¦Ã‚Â¡Ãƒâ€šÃ‚Â  {concerns[:100]}")
    else:
        print("\n  [no judge data]")

    print()


