def main() -> None:
    parser = argparse.ArgumentParser(
        description="View or re-judge existing UE benchmark snapshots"
    )
    group = parser.add_mutually_exclusive_group(required=True)
    group.add_argument(
        "--snapshot",
        metavar="PATH",
        help="Path to a specific test_result snapshot directory",
    )
    group.add_argument(
        "--task",
        metavar="TASK_ID",
        help="Show all snapshots for this task ID (e.g. ue_task_0002)",
    )
    parser.add_argument(
        "--rejudge",
        action="store_true",
        help="Re-run the LLM judge on the snapshot(s) and update result.json",
    )
    parser.add_argument(
        "--judge-model",
        default=os.environ.get("UNREALBENCH_JUDGE_MODEL", ""),
        help="Judge model to use with --rejudge, or UNREALBENCH_JUDGE_MODEL.",
    )
    args = parser.parse_args()

    if args.snapshot:
        snap = Path(args.snapshot)
        if not snap.is_absolute():
            snap = Path.cwd() / snap
        if not snap.exists():
            snap = REPO_ROOT / args.snapshot
        if not snap.exists():
            # treat as a prefix/task-id and glob for matching snapshots
            matches = sorted(RESULTS_DIR.glob(f"{args.snapshot}*"))
            matches = [m for m in matches if m.is_dir()]
            if not matches:
                print(f"No snapshot found matching '{args.snapshot}'", file=sys.stderr)
                sys.exit(1)
            if len(matches) > 1:
                # show all matches, same as --task
                for snap in matches:
                    if args.rejudge:
                        _rejudge(snap, args.judge_model)
                    else:
                        _print_results(snap)
                return
            snap = matches[0]
        if args.rejudge:
            _rejudge(snap, args.judge_model)
        else:
            _print_results(snap)

    elif args.task:
        task_id = args.task
        snapshots = sorted(RESULTS_DIR.glob(f"{task_id}_*"))
        if not snapshots:
            print(f"No snapshots found for {task_id} in {RESULTS_DIR}", file=sys.stderr)
            sys.exit(1)
        for snap in snapshots:
            if args.rejudge:
                _rejudge(snap, args.judge_model)
            else:
                _print_results(snap)


if __name__ == "__main__":
    main()

